char	*ft_strcapitalize(char *str)
{
	int		n;

	if (*str != '\0' && *str >= 'a' && *str <= 'z')
		*str = *str - 32;
	n = 1;
	while (*(str + n) != '\0')
	{
		if (((*(str + n) >= 'a' && *(str + n) <= 'z') || \
			(*(str + n) >= 'A' && *(str + n) <= 'Z')) && \
			((*(str + n - 1) < '0') || \
			(*(str + n - 1) > '9' && *(str + n - 1) < 'A') || \
			(*(str + n - 1) > 'Z' && *(str + n - 1) < 'a') || \
			*(str + n - 1) > 'z'))
		{
			if (*(str + n) >= 'a' && *(str + n) <= 'z')
				*(str + n) = *(str + n) - 32;
		}
		else if (*(str + n) >= 'A' && *(str + n) <= 'Z')
			*(str + n) = *(str + n) + 32;
		n ++;
	}
	return (str);
}
